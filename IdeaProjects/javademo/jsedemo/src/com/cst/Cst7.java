package com.cst;

public class cst7 {
    public static void main(String[] args) {
        boolean a=true;//a同意
        boolean b=false;//b反对
        boolean c=false;//c反对
        boolean d=true;//d同意
        System.out.println(a&b&c&d);
        System.out.println(a|b|c|d);
        System.out.println(a&&b&&c&&d);
        System.out.println(a||b||c||d);
    }
}
